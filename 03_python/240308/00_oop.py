# OOP : Object Oriented Programming (객체 지향 프로그래밍)

class Animal:
    # 클래스 변수
    # 클래스 함수
    # 생성자
    # 인스턴스 함수
    pass

pig = Animal()
print(pig) # <__main__.Animal object at 0x00000269395BA270> -> 저장된 RAM의 주소를 나타낸다.
print(type(pig)) # <class '__main__.Animal'> -> Animal 이라는 클래스를 만들었다.

num = 1
print(type(num)) # <class 'int'> -> int라는 클래스는 기존에 존재.

